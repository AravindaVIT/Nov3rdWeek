package com.world;

public class Operators {
	public static void main(String[] args){
		int i=10;
		System.out.println("i=" + i);
		int i1=+i;
		System.out.println("i=" + i);
		int i2= ++i;
		System.out.println("i=" + i);
		int i3= i++;
		System.out.println("i=" + i);
		System.out.println("i3=" + i3);
		
		System.out.println("i=" + i);
		System.out.println("i1=" + i1);
		
	}

}
